async function refreshCache(){
  const [props, income, expenses, contacts, notes, settingsList] = await Promise.all([
    client.models.Property.list(),
    client.models.IncomeEntry.list(),
    client.models.Expense.list(),
    client.models.Contact.list(),
    client.models.Note.list(),
    client.models.Settings.list(),
  ]);
  cache.properties = props.data;
  cache.income = income.data;
  cache.expenses = expenses.data;
  cache.contacts = contacts.data;
  cache.notes = notes.data;
  if(settingsList.data.length){
    cache.settings = settingsList.data[0];
  } else {
    const created = await client.models.Settings.create({ appName: 'Rent Ledger', currency: 'INR' });
    cache.settings = created.data;
  }
}

