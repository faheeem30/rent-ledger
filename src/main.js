function renderNotes(main){
  const D = cache;
  const sorted = [...D.notes].sort((a,b)=>(a.done===b.done?0:a.done?1:-1));
  main.innerHTML = `
    <div class="page-head">
      <div><h2>Upcoming Plans</h2><div class="sub">Reminders and to-dos that don't belong to one property — renewals, planned repairs, ideas</div></div>
    </div>
    <div class="card">
      <div class="field-row" style="align-items:flex-end;">
        <div class="field" style="flex:2;"><label>What's coming up?</label><input id="note-text" placeholder="e.g. Renew Green Villa agreement in August"></div>
        <div class="field"><label>Target Date</label><input id="note-date" type="date"></div>
        <button class="btn" id="note-add-btn" style="margin-bottom:13px;" onclick="withLoading(document.getElementById('note-add-btn'), addNote)">Add</button>
      </div>
    </div>
    <div class="card">
      ${sorted.length===0?'<div class="empty">No upcoming plans yet.</div>':sorted.map(n=>`
        <div class="note-item">
          <input type="checkbox" ${n.done?'checked':''} onchange="toggleNote('${n.id}')">
          <div class="txt ${n.done?'done':''}">${n.text}${n.date?` <span class="date">· ${n.date}</span>`:''}</div>
          <button class="btn tiny danger" onclick="deleteNote('${n.id}')">Remove</button>
        </div>
      `).join('')}
    </div>
  `;
}

async function addNote(){
  const text = document.getElementById('note-text').value.trim();
  if(!text) return;
  await client.models.Note.create({ text, date: document.getElementById('note-date').value, done:false });
  await refreshCache();
  render();
}
async function toggleNote(id){
  const n = cache.notes.find(x=>x.id===id);
  await client.models.Note.update({ id, done: !n.done });
  await refreshCache();
  render();
}
async function deleteNote(id){
  await client.models.Note.delete({ id });
  await refreshCache();
  render();
}

