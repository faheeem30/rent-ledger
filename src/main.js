function drawChart(){
  const canvas = document.getElementById('mainChart');
  if(!canvas || typeof Chart === 'undefined') return;
  const D = cache;
  const months = [];
  const d = new Date();
  for(let i=5;i>=0;i--){
    const dt = new Date(d.getFullYear(), d.getMonth()-i, 1);
    months.push(dt.toISOString().slice(0,7));
  }
  const incomeData = months.map(m=>D.income.filter(i=>i.month===m).reduce((s,i)=>s+Number(i.amount),0));
  const expenseData = months.map(m=>D.expenses.filter(e=>e.date && e.date.slice(0,7)===m).reduce((s,e)=>s+Number(e.cost),0));
  if(chartInstance) chartInstance.destroy();
  chartInstance = new Chart(canvas.getContext('2d'), {
    type:'bar',
    data:{ labels: months.map(monthLabel), datasets:[
        {label:'Income', data:incomeData, backgroundColor:'#4C6B54', borderRadius:4, maxBarThickness:34},
        {label:'Expenses', data:expenseData, backgroundColor:'#A8462F', borderRadius:4, maxBarThickness:34}
    ]},
    options:{ responsive:true, maintainAspectRatio:false,
      plugins:{legend:{position:'bottom', labels:{font:{family:'Inter'}, boxWidth:12}}},
      scales:{ y:{beginAtZero:true, grid:{color:'#D6CDB2'}, ticks:{font:{family:'IBM Plex Mono'}, callback:v=>CURRENCIES[cache.settings.currency||'INR'].symbol.trim()+v}},
        x:{grid:{display:false}, ticks:{font:{family:'IBM Plex Mono'}}} } }
  });
}

