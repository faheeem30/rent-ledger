function withLoading(btn, fn){
  if(!btn || btn.dataset.busy) return;
  btn.dataset.busy = '1';
  const orig = btn.textContent;
  btn.disabled = true;
  btn.textContent = 'Saving…';
  Promise.resolve().then(fn).catch(e=>{ console.error(e); alert(e.message || 'Something went wrong — please try again.'); })
    .finally(()=>{ try{ btn.disabled = false; btn.textContent = orig; delete btn.dataset.busy; }catch(e){} });
}
