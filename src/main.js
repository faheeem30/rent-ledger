function setAuthScreen(s){ authScreen = s; renderAuth(); }

async function doSignup(){
  const email = document.getElementById('su-email').value.trim().toLowerCase();
  const pass = document.getElementById('su-pass').value;
  const err = document.getElementById('su-error');
  try{
    await signUp({ username: email, password: pass, options: { userAttributes: { email } } });
    pendingConfirmEmail = email;
    authScreen = 'confirm';
    renderAuth();
  }catch(e){ err.textContent = e.message || 'Could not create account.'; }
}
async function doConfirm(){
  const err = document.getElementById('cf-error');
  try{
    await confirmSignUp({ username: pendingConfirmEmail, confirmationCode: document.getElementById('cf-code').value.trim() });
    authScreen = 'signin';
    renderAuth();
  }catch(e){ err.textContent = e.message || 'Could not verify code.'; }
}
async function doLogin(){
  const err = document.getElementById('lg-error');
  try{
    await signIn({
      username: document.getElementById('lg-email').value.trim().toLowerCase(),
      password: document.getElementById('lg-pass').value,
    });
    await startApp();
  }catch(e){ err.textContent = e.message || 'Could not sign in.'; }
}
async function doForgotRequest(){
  const err = document.getElementById('fp-error');
  const email = document.getElementById('fp-email').value.trim().toLowerCase();
  try{
    await resetPassword({ username: email });
    forgotEmail = email;
    authScreen = 'forgot-confirm';
    renderAuth();
  }catch(e){ err.textContent = e.message || 'Could not start password reset.'; }
}
async function doForgotConfirm(){
  const err = document.getElementById('fp-confirm-error');
  try{
    await confirmResetPassword({
      username: forgotEmail,
      confirmationCode: document.getElementById('fp-code').value.trim(),
      newPassword: document.getElementById('fp-newpass').value,
    });
    forgotEmail = null;
    authScreen = 'signin';
    renderAuth();
  }catch(e){ err.textContent = e.message || 'Could not reset password.'; }
}
async function doSignOut(){ await signOut(); currentUser = null; authScreen = 'signin'; renderAuth(); }
async function doDeleteAccount(){
  try{
    await Promise.all([
      ...cache.properties.map(p=>client.models.Property.delete({id:p.id})),
      ...cache.income.map(i=>client.models.IncomeEntry.delete({id:i.id})),
      ...cache.expenses.map(e=>client.models.Expense.delete({id:e.id})),
      ...cache.contacts.map(c=>client.models.Contact.delete({id:c.id})),
      ...cache.notes.map(n=>client.models.Note.delete({id:n.id})),
    ]);
    if(cache.settings.id) await client.models.Settings.delete({id: cache.settings.id});
    await deleteUser();
  }catch(e){ console.error(e); }
  currentUser = null;
  authScreen = 'signin';
  closeModal();
  renderAuth();
}

