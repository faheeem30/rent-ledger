async function findCoords(){
  const address = document.getElementById('f-address').value.trim();
  const status = document.getElementById('geo-status');
  if(!address){ status.textContent = 'Type an address first.'; status.style.color = '#A8462F'; return; }
  status.textContent = 'Searching…'; status.style.color = '#8a8468';
  try{
    const res = await fetch('https://nominatim.openstreetmap.org/search?format=json&limit=1&q=' + encodeURIComponent(address));
    const data = await res.json();
    if(data && data.length){
      document.getElementById('f-lat').value = data[0].lat;
      document.getElementById('f-lng').value = data[0].lon;
      status.textContent = '✓ Location found and filled in below.'; status.style.color = '#4C6B54';
    } else {
      status.textContent = 'Could not find that address — try adding city/state, or enter coordinates manually below.'; status.style.color = '#A8462F';
    }
  }catch(e){
    status.textContent = 'Lookup failed — check your connection, or enter coordinates manually below.'; status.style.color = '#A8462F';
  }
}

