function openContactModal(propertyId, contactId){
  const D = cache;
  const c = contactId ? D.contacts.find(x=>x.id===contactId) : null;
  if(c) propertyId = c.propertyId;
  const opts = D.properties.map(p=>`<option value="${p.id}" ${p.id===propertyId?'selected':''}>${p.name}${p.address?' — '+p.address:''}</option>`).join('');
  const isCustomType = c && !CONTACT_TYPES.includes(c.type);
  const typeOpts = CONTACT_TYPES.map(t=>`<option ${c&&c.type===t?'selected':''}>${t}</option>`).join('');
  const modal = document.createElement('div');
  modal.className = 'overlay';
  modal.innerHTML = `
    <div class="modal">
      <h3>${c?'Edit Contact':'Add Contact'}</h3>
      <div class="field"><label>Property</label><select id="c-prop">${opts}</select></div>
      <div class="field-row">
        <div class="field"><label>Type / Occupation</label>
          <select id="c-type" onchange="document.getElementById('c-type-other').style.display=this.value==='Other'?'block':'none';">
            ${typeOpts}<option value="Other" ${isCustomType?'selected':''}>Other</option>
          </select>
        </div>
        <div class="field"><label>Name</label><input id="c-name" value="${c?c.name:''}" placeholder="e.g. Suresh"></div>
      </div>
      <div class="field" id="c-type-other" style="display:${isCustomType?'block':'none'};"><label>Type the occupation</label><input id="c-type-custom" value="${isCustomType?c.type:''}" placeholder="e.g. Locksmith"></div>
      <div class="field"><label>Phone</label><input id="c-phone" value="${c?c.phone||'':''}" placeholder="e.g. 98765 43210"></div>
      <div class="field"><label>Notes</label><input id="c-notes" value="${c?c.notes||'':''}" placeholder="optional"></div>
      <div class="modal-actions">
        ${c?'<button class="btn danger" onclick="deleteContact(\''+c.id+'\')">Delete</button>':''}
        <button class="btn ghost" onclick="closeModal()">Cancel</button>
        <button class="btn" id="contact-save-btn" onclick="withLoading(document.getElementById('contact-save-btn'), ()=>saveContact('${c?c.id:''}'))">Save</button>
      </div>
    </div>`;
  document.body.appendChild(modal);
}

async function saveContact(id){
  const typeVal = document.getElementById('c-type').value;
  const type = typeVal==='Other' ? (document.getElementById('c-type-custom').value.trim() || 'Other') : typeVal;
  const data = {
    propertyId: document.getElementById('c-prop').value,
    type,
    name: document.getElementById('c-name').value.trim() || 'Unnamed',
    phone: document.getElementById('c-phone').value.trim(),
    notes: document.getElementById('c-notes').value.trim()
  };
  if(id){ await client.models.Contact.update({ id, ...data }); }
  else { await client.models.Contact.create(data); }
  await refreshCache();
  closeModal();
  render();
}

async function deleteContact(id){
  await client.models.Contact.delete({ id });
  await refreshCache();
  closeModal();
  render();
}

